public class Calculator {

    public int addNum(int num1, int num2){
        return num1 + num2;
    }

    public int subNum(int num1, int num2){
        return num1 - num2;
    }

    public int multNum(int num1, int num2){
        return num1 * num2;
    }

    public int divNum(int num1, int num2){
        return num1 / num2;
    }
}
